int	ft_str_is_numeric(char *str)
{
	int	i;
	int	x;
	int	ret;

	i = 0;
	x = 1;
	while (str[i] != '\0')
	{
		if (str[i] >= 48 && str[i] <= 57)
			x++;
		else
		{
			x = 0;
			break ;
		}
		i++;
	}
	if (x == 0)
		ret = 0;
	else
		ret = 1;
	return (ret);
}
