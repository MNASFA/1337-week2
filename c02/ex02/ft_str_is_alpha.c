int	ft_str_is_alpha(char *str)
{
	int	i;
	int	x;
	int	ret;

	x = 1;
	while (str[i] != '\0')
	{
		if (str[i] >= 97 && str[i] <= 122)
			x++;
		else if (str[i] >= 65 && str[i] <= 90)
			x++;
		else
		{
			x = 0;
			break ;
		}
		i++;
	}
	if (x == 0)
		ret = 0;
	else
		ret = 1;
	return (ret);
}
